#Rounding Decimals 5 ways of rounding off:
#1 truncation:

import numpy as np
Atif = np.trunc([-3.666, 3.6667]) 
print(Atif)


import numpy as np
Atif = np.fix([-3.666, 3.6667]) 
print(Atif)

import numpy as np
Atif = np.round([-3.666, 3.6667]) 
print(Atif)

import numpy as np
Atif = np.floor([-3.666, 3.6667]) 
print(Atif)

import numpy as np
Atif = np.ceil([-3.666, 3.6667]) 
print(Atif)

